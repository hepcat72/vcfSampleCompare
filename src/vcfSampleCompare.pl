    my $group1      = $_[0];
    my $group2      = $_[1];
    my $sample_info = $_[2];

    my $gt_score   = 0;  #Lowest possible is 0 when groups are supplied
    my $or_score   = -2; #Lowest possible is -1 when groups are supplied
